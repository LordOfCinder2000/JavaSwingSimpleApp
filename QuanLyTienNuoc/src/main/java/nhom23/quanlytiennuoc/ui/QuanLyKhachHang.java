    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaCT, sb, "Mã công tơ không được để trống");
        if(sb.length()>0)
        {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        if(MessageDialogHelper.showConfirmDialog(parentForm, "Bạn có muốn xóa khách hàng không?", "Hỏi") == JOptionPane.NO_OPTION)
        {
            return;
        }
        try {
            
            KhachHangDao dao = new KhachHangDao();
            if(dao.Delete(txtMaCT.getText()))
            {
                MessageDialogHelper.showMessageDialog(parentForm, "Khách hàng đã được xóa", "Thông báo");
                btnNewActionPerformed(evt);
            }
            else
            {
                MessageDialogHelper.showConfirmDialog(parentForm, "Khách hàng chưa được xóa", "Thông báo");
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblKhachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhachHangMouseClicked
        try {
            int row = tblKhachHang.getSelectedRow();
            
            if(row >= 0)
            {
                String id = (String) tblKhachHang.getValueAt(row, 0);
                KhachHangDao dao = new KhachHangDao();
                KhachHang kh = dao.findByID(id);
                
                if(kh != null)
                {
                    txtMaCT.setText(kh.getMaCT());
                    txtMaCT.setEditable(false);;
                    txtHoten.setText(kh.getHoTen());
                    txtDiaChi.setText(kh.getSoNha());
                    txtSoDienThoai.setText(kh.getSoDienThoai());
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_tblKhachHangMouseClicked


