    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaCT, sb, "Mã công tơ không được để trống");
        DataValidator.validateEmpty(txtHoten, sb, "Tên không được để trống");
        DataValidator.validateEmpty(txtDiaChi, sb, "Địa chỉ không được để trống");
        DataValidator.validateEmpty(txtSoDienThoai, sb, "Số điện thoại không được để trống");
        DataValidator.validatePhoneNumber(txtSoDienThoai, sb);
        if(sb.length()>0)
        {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        KhachHangDao dao = null;
        try {
            KhachHang kh = new KhachHang();
            kh.setMaCT(txtMaCT.getText());
            kh.setHoTen(txtHoten.getText());
            kh.setSoNha(txtDiaChi.getText());
            kh.setSoDienThoai(txtSoDienThoai.getText());
            dao = new KhachHangDao();
            if(dao.Insert(kh))
            {
                MessageDialogHelper.showMessageDialog(parentForm, "Khách hàng đã được lưu", "Thông báo");
                loadDataToTable();
                for (int i = 0; i < tblKhachHang.getModel().getRowCount(); i++) {
                    if(tblKhachHang.getModel().getValueAt(i, 0).equals(txtMaCT.getText()))
                    {
                        tblKhachHang.setRowSelectionInterval(i, i);
                        break;
                    }
                    
                }
            }
            else
            {
                MessageDialogHelper.showConfirmDialog(parentForm, "Khách hàng chưa được lưu", "Thông báo");
            }
        } catch (Exception e) {
            e.printStackTrace();
            if(dao.findByID(txtMaCT.getText()) != null)
            {
                MessageDialogHelper.showErrorDialog(parentForm, "Đã có mã công tơ trong cơ sở dự liệu", "Lỗi");
            }
            else
                MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

