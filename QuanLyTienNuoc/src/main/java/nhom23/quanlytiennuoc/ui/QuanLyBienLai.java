    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(txtThanhTien.getText() != null || txtThanhTien.getText() != "")
            txtThanhTien.setText("");
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaBL, sb, "Mã biên lai không được để trống");
        DataValidator.validateEmpty(txtChiSoMoi, sb, "Chỉ số nước mới không được để trống");
        DataValidator.validateNumber(txtChiSoMoi, sb);
        DataValidator.validateEmpty(txtChiSoCu, sb, "Chỉ số nước cũ không được để trống");
        DataValidator.validateNumber(txtChiSoCu, sb);
        DataValidator.validateEmpty(txtNgayLap, sb, "Ngày lập không được để trống");
        DataValidator.validateDateTime(txtNgayLap, sb, "Ngày phải nhập theo định dạo dd/MM/yyyy");
        DataValidator.validateEmpty(cbbMaCT, sb, "Phải chọn mã công tơ");
        DataValidator.validateEmpty(cbbMaTK, sb, "Phải chọn người lập biên lai");
        if(sb.length()>0)
        {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        BienLaiDao dao = null;
        try {
            BienLai bl = new BienLai();
            bl.setMaBL(txtMaBL.getText());
            bl.setChiSoMoi(Integer.parseInt(txtChiSoMoi.getText()));
            var csm = Integer.parseInt(txtChiSoMoi.getText());
            bl.setChiSoCu(Integer.parseInt(txtChiSoCu.getText()));
            var csc = Integer.parseInt(txtChiSoCu.getText());
            if(((csm-csc)*668) < 0)
            {
                MessageDialogHelper.showMessageDialog(parentForm, "Chỉ số mới không được nhỏ hợn chỉ số cũ", TOOL_TIP_TEXT_KEY);
                return;
            }
            bl.setTongTien(((csm-csc)*668));
            
            bl.setNgayLap(DataValidator.formatDate(txtNgayLap.getText()));
            
            bl.setMaCT(((KhachHang)cbbMaCT.getSelectedItem()).getMaCT());
            bl.setMaTK(((NguoiDung)cbbMaTK.getSelectedItem()).getMaTK());
            dao = new BienLaiDao();
            
            if(dao.Insert(bl))
            {
                MessageDialogHelper.showMessageDialog(parentForm, "Biên lai đã được lưu", "Thông báo");
                loadDataToTable();
                for (int i = 0; i < tblBienLai.getModel().getRowCount(); i++) {
                    if(tblBienLai.getModel().getValueAt(i, 0).equals(txtMaBL.getText()))
                    {
                        tblBienLai.setRowSelectionInterval(i, i);
                        break;
                    }
                    
                }
            }
            else
            {
                MessageDialogHelper.showConfirmDialog(parentForm, "Biên lai chưa được lưu", "Thông báo");
            }
        } catch (Exception e) {
            e.printStackTrace();
            if(dao.findByID(txtMaBL.getText()) != null)
            {
                MessageDialogHelper.showErrorDialog(parentForm, "Đã có mã biên lai trong cơ sở dự liệu", "Lỗi");
            }
            else
                MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
            
        }
    }//GEN-LAST:event_btnSaveActionPerformed

