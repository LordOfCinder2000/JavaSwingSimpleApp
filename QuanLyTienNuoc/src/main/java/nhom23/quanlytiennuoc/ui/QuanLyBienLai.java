    private void tblBienLaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBienLaiMouseClicked
        try {
            int row = tblBienLai.getSelectedRow();
            
            if(row >= 0)
            {
                String id = (String) tblBienLai.getValueAt(row, 0);
                BienLaiDao dao = new BienLaiDao();
                KhachHangDao khachHangDao = new KhachHangDao();
                NguoiDungDao nguoiDungDao = new NguoiDungDao();
                BienLai bl = dao.findByID(id);
                
                if(bl != null)
                {
                    txtMaBL.setText(bl.getMaBL());
                    txtChiSoMoi.setText(String.valueOf(bl.getChiSoMoi()));
                    txtChiSoCu.setText(String.valueOf(bl.getChiSoCu()));
                    txtThanhTien.setText(String.valueOf(bl.getTongTien()));
                    
                    txtNgayLap.setText(DataValidator.formatDateForTable(bl.getNgayLap()));
                    
                    cbbBoxModelKH.setSelectedItem(khachHangDao.findByID(bl.getMaCT()));
                    cbbBoxModelND.setSelectedItem(nguoiDungDao.findByID(bl.getMaTK()));
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_tblBienLaiMouseClicked

    private void btnTinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhActionPerformed
        if(txtThanhTien.getText() != null || txtThanhTien.getText() != "")
            txtThanhTien.setText("");
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtChiSoMoi, sb, "Chỉ số nước mới không được để trống");
        DataValidator.validateNumber(txtChiSoMoi, sb);
        DataValidator.validateEmpty(txtChiSoCu, sb, "Chỉ số nước cũ không được để trống");
        DataValidator.validateNumber(txtChiSoCu, sb);
        if(sb.length()>0)
        {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        var csm = Integer.parseInt(txtChiSoMoi.getText());
        var csc = Integer.parseInt(txtChiSoCu.getText());
        var kq = (csm - csc)*668;
        if(kq > 0)
        {
            txtThanhTien.setText(String.valueOf(kq));
        }
        else
        {
            MessageDialogHelper.showMessageDialog(parentForm, "Chỉ số mới không được nhỏ hợn chỉ số cũ", "Thông báo");
        }
        
        
    }//GEN-LAST:event_btnTinhActionPerformed

